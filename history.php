	<center> <h1>History</h1>
	  <img src="school7.jpg" style="height:400px;">
	     <h3> আনোয়ার আলী মেমোরিয়াল স্কুল এন্ড কলেজে</h3>
		 <p> চাঁদপুরের হাজীগঞ্জে নিজের পরিবারের টাকার আর শ্রমে চলছে 'খোকন বিএসসির পাঠশালা'। নিজ অর্থায়নে জমি কিনে ২০১৪ সালে মাধ্যমিক বিদ্যালয়টি প্রতিষ্ঠা করেন বীর মুক্তিযোদ্ধা আবদুর রব খোকন। প্রতিষ্ঠার পর থেকে শিক্ষকদের নিয়মিত বেতন প্রদান, অবকাঠামো উন্নয়ন, নিজে অবৈতনিক অধ্যক্ষ পদে থেকে নিজের দুই ছেলেকে দিয়ে বিনাবেতন পড়াচ্ছেন এলাকার হাজার হাজার ছেলে-মেয়েকে। এলাকাকে আলোকিত করে চলছেন নিজের পরিবারের অর্থ আর শ্রমে। শিক্ষকতা পেশার দায় থেকে বিদ্যালয়টি প্রতিষ্ঠা করেছেন। এখন বিদ্যালয়টিকে সামনের দিকে এগিয়ে নিতে যেন দায়িত্ব যেন কাঁধে পড়েছে। সরকারিভাবে পাঠ দানের অনুমতিসহ এমপিওভুক্তির জন্য সরকারের সংশ্লিষ্ট দপ্তরে দৌড়াচ্ছেন বীর মুক্তিযোদ্ধা খোকন। এটি খোকন বিএসসির পাঠশালা হিসেবে এলাকায় পরিচিতি পেলেও এর কাগুজে নাম 'আনোয়ার আলী মেমোরিয়াল স্কুল এন্ড কলেজ'।
 
খোঁজ নিয়ে জানা যায়, আবদুর রব খোকন এলাকায় খোকন বিএসসি হিসেবে পরিচিতি। শিক্ষকতা পেশায় থাকাকালীন জাতির জনকের ডাকে চলে যান মুক্তিযুদ্ধে। যুদ্ধ চলাকালীন ভারতে ট্রেনিং গ্রহণ করে এলাকায় এসে জড়িয়ে পড়েন সরাসরি মুক্তিযুদ্ধে। বেশ কয়েকবার পাক বাহিনীর সাথে সরাসরি যুদ্ধে অংশ নিয়েছেন। যুদ্ধচলাকালীন দুই উপজেলার সহকারী কমান্ডার হিসেবে দায়িত্ব পালন করেছেন। স্বাধীনতার পর হাজীগঞ্জ উপজেলা মুক্তিযুদ্ধ কমান্ড কাউন্সিলের দুই বারের কমান্ডার হয়েছিলেন। যুদ্ধ শেষে ফিরে গেছেন সেই পুরনো পেশা শিক্ষকতায়। শিক্ষকতা পেশায় থেকে ছেলেদেরকে গড়ে তুলেছেন নিজেদের মতো করে। ছেলেদের সবাই প্রতিষ্ঠিত। একজন সরকারের গুরুত্বপূর্ণ পদে দায়িত্ব পালন করছেন। শিক্ষকতা পেশা থেকে অবসর নিয়ে নেমে পড়েন নিজের স্বপ্ন পূরণে, তথা স্কুল প্রতিষ্ঠার কাজে। গড়ে তোলেন নিজ বাবার নামে স্কুল। 
 
আব্দুর রব খোকন চাঁদপুরের হাজীগঞ্জের বাকিলা ইউনিয়নের টেকের বাজার এলাকায় গড়ে তোলেন 'আনোয়ার আলী মেমোরিয়াল স্কুল এন্ড কলেজ'। ২০১৪ সালে ৭৪ শতাংশ জমির ওপর গড়ে উঠে বিদ্যালয়টি। শুরুর দিকে ছাত্র-ছাত্রী কিছুটা কম হলেও এর পড়ালেখার মান দেখে ধীরে ধীরে শিক্ষার্থী বাড়তে থাকে। বর্তমানে এখানে ৬ শতাধিক শিক্ষার্থী অধ্যয়নরত রয়েছে। বিদ্যালয়ের পাশের ৫-৬ কিলোমিটারের মধ্যে মাধ্যমিক পর্যায়ের কোন শিক্ষাপ্রতিষ্ঠান না থাকার কারণে অত্র অঞ্চলের শিক্ষার্থীরা এখানেই পড়ার সুযোগ পাচ্ছে।  
 
অবকাঠামোগত দিক দিয়ে ৩ হাজার স্কয়ার ফিটের একটি সেমিপাকা একটি একাডেমিক ভবন ও সমমানের টিনের তৈরি আরেকটি একাডেমিক ভবন থাকাতে ক্লাশ নিতে তেমন সমস্য হয়নি। ৬শ শিক্ষার্থীর জন্য শিক্ষক রয়েছে ১৪ জন। এই ১৪ জন শিক্ষককে নিয়মিত বেতন দিয়ে যাচ্ছেন প্রতিষ্ঠাতার পরিবারের পক্ষ থেকে। 
 
খোঁজ নিয়ে জানা যায়, বীর মুক্তিযোদ্ধা আব্দুর রব খোকন বিএসসি নিজে প্রতিষ্ঠানের অধ্যড়্গ হিসেবে প্রতিষ্ঠানকিকে দ্বাড় করানোর জন্য নিরলসভাবে কাজ করছেন। এই প্রতিষ্ঠাতার অপর দুই ছেলে গ্রাজুয়েশান শেষ করার পর থেকে এই শিড়্গা প্রতিষ্ঠানে শিড়্গকতা পেশায় রয়েছে। প্রতিষ্ঠাতাদের এই ৩ জনই শুরম্ন থেকে অবৈতনিক হিসেবে শিড়্গকতা করছেন।
 
আনোয়ার আলী স্কুল এন্ড কলেজের অবৈতনিক প্রতিষ্ঠাতা অধ্যক্ষ বীর মুক্তিযোদ্ধা আব্দুর রব খোকন বিএসসি বলেন, আমি স্কুলটি প্রতিষ্ঠা করেছি এলাকাকে আরো আলোকিত করতে। এই প্রতিষ্ঠানে পড়ালেখা করে এলাকার ছেলে মেয়েরা দেশের উন্নয়নে কাজ করবে সেইটা আমার প্রত্যাশা। পাঠ দানের অনুমতিসহ বিদ্যালয়টি এমপিওভুক্ত হলে আমরা আরো মনের সাহস নিয়ে কাজ করতে পারবো। সরকারের প্রতি আকুল আবেদন সরকার যেন আমাদের এই বিদ্যালয়টিকে পাঠ দানের অনুমতিসহ এমপিও করার ব্যবস্থা করে দেয়। 
 
আনোয়ার আলী মেমোরিয়াল স্কুল এন্ড কলেজের পাঠদানের অনুমতির বিষয়ে উপজেলা মাধ্যমিক শিক্ষ কর্মকর্তা মোস্তাফিজুর রহমান জানান, এ বিষয়ে আমাদের কাছে একটি আবেদন এসেছিল আমরা আরো আগে মন্ত্রণালয়ে পাঠিয়েছি। 
